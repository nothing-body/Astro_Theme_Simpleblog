let currentLanguage = normalizeLanguage(
  process.argv.find(arg => arg.startsWith("--lang="))?.slice("--lang=".length) ||
    process.env.DEPLOY_LANG ||
    "zh-tw"
);

export function normalizeLanguage(value) {
  const raw = String(value || "").trim().toLowerCase();
  if (["en", "english"].includes(raw)) return "en";
  if (["zh", "zh-tw", "tw", "traditional", "traditional-chinese"].includes(raw)) return "zh-tw";
  return "zh-tw";
}

export function setLanguage(value) {
  currentLanguage = normalizeLanguage(value);
  process.env.DEPLOY_LANG = currentLanguage;
  return currentLanguage;
}

export function getLanguage() {
  return currentLanguage;
}

export function t(key, vars = {}) {
  const template = dictionaries[currentLanguage]?.[key] ?? dictionaries.en[key] ?? key;
  return template.replace(/\{([A-Za-z0-9_]+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function stripLangArgs(argv) {
  return argv.filter(arg => !arg.startsWith("--lang="));
}
